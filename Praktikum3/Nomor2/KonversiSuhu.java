public class KonversiSuhu {
    public double celciusToFahrenheit(double c){
        double hasil = (9.00 / 5.00) * c + 32;
        return hasil;
    }
    
    public double celciusToReamur(double c){
        double hasil = (4.00 / 5.00) * c;
        return hasil;
    }
}
